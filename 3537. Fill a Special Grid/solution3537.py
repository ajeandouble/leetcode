from typing import List


class Solution:
    def specialGrid(self, n: int) -> List[List[int]]:
        n_square = pow(2, n)
        if n == 0:
            return [[0]]
        grid = [[-1] * n_square for _ in range(n_square)]
        count = 0

        def helper(r, c, i):
            nonlocal count
            if i == 0:
                grid[r][c] = count
                count += 1
                return
            helper(r, c, i - 1)
            helper(r + pow(2, i - 1), c, i - 1)
            helper(r + pow(2, i - 1), c - pow(2, i - 1), i - 1)
            helper(r, c - pow(2, i - 1), i - 1)

        helper(0, n_square - 1, n)
        return grid


